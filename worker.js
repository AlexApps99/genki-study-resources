self.addEventListener('install', (e) => {
  e.waitUntil((async () => {
    // Caches files of the site for offline usage
    const cache = await caches.open(CACHE_NAME);
    await cache.addAll(CACHED_FILES);
  })());
  // Force the waiting service worker to become the active service worker.
  self.skipWaiting();
});

self.addEventListener("activate", (e) => {
  e.waitUntil((async () => {
    // Delete old caches
    caches.keys().then((keyList) => {
      return Promise.all(keyList.map((key) => {
        if (key !== CACHE_NAME) {
          return caches.delete(key);
        }
      }));
    })

    // Enable navigation preload if it's supported.
    // See https://developers.google.com/web/updates/2017/02/navigation-preload
    if ("navigationPreload" in self.registration) {
      await self.registration.navigationPreload.enable();
    }
  })());

  // Tell the active service worker to take control of the page immediately.
  self.clients.claim();
});

self.addEventListener("fetch", (e) => {
  e.respondWith((async () => {
    const cache = await caches.open(CACHE_NAME);
    try {
      // First, try to use the navigation preload response if it's supported.
      const preloadResponse = await e.preloadResponse;
      if (preloadResponse) {
        await cache.put(e.request, preloadResponse.clone());
        return preloadResponse;
      }

      // Always try the network first.
      const networkResponse = await fetch(e.request);
      await cache.put(e.request, networkResponse.clone());
      return networkResponse;
    } catch (error) {
      // catch is only triggered if an exception is thrown, which is likely
      // due to a network error.
      // If fetch() returns a valid HTTP response with a response code in
      // the 4xx or 5xx range, the catch() will NOT be called.
      console.log("Fetch failed; returning offline page instead.", error);

      let cachedResponse = await cache.match(e.request);
      if (!cachedResponse && e.request.url.endsWith("/")) {
        const newUrl = e.request.url + "index.html";
        console.log("Correcting cache URL to:", newUrl);
        cachedResponse = await cache.match(newUrl);
      }
      return cachedResponse;
    }
  })());
});
